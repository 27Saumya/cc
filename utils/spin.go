package utils

import (
	"time"

	"github.com/briandowns/spinner"
)

var spin = []string{
	`[|\___________]`,
	`[_|\__________]`,
	`[__|\_________]`,
	`[___|\________]`,
	`[____|\_______]`,
	`[_____|\______]`,
	`[______|\_____]`,
	`[_______|\____]`,
	`[________|\___]`,
	`[_________|\__]`,
	`[__________|\_]`,
	`[___________|\]`,
	`[___________/|]`,
	`[__________/|_]`,
	`[_________/|__]`,
	`[_______ /|___]`,
	`[_______/|____]`,
	`[______/|_____]`,
	`[_____/|______]`,
	`[____/|_______]`,
	`[___/|________]`,
	`[__/|_________]`,
	`[_/|__________]`,
	`[/|___________]`,
}

func MakeItSpin(f func(), suffix string) {

	s := spinner.New(spin, 75*time.Millisecond)
	s.Suffix = " " + Colorize("blue", suffix)
	s.Start()

	f()

	time.Sleep(time.Second)

	s.Stop()

}
